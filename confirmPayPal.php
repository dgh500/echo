<h3>What Is A Confirmed PayPal Address?</h3>
If you choose to use PayPal to place your order we request that you use a PayPal confirmed address. This means that PayPal have verified that your address to your
credit or debit card.
<br><br>
Failure to do so will delay the dispatch of your order as we
will have to contact you for proof of address (utlity bill or similar). If you do not have a confirmed PayPal address we very much recommend you checkout using
our bespoke checkout system which does not have this limitation.
<br><br>
